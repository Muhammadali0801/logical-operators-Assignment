// LOGICAL STATEMENTS;
 //  USING AND (&&) OPERATOR; 
 let A: number = 12;
 let B: number = 12;
 console.log(A == 12 && B == 12);  // Output: true;
 
 // USING OR(||) OPERATOR;
 let num1: number = 15;
 let num2: number = 18;
 console.log(num1 == 15 || num2 == num1); // Output: true;
 
 // USING NOT (!) OPERATOR;
 let FIRSTNAME: string = "ali" ;
 let SECONDNAME: string = "ali" ;
 console.log(!(FIRSTNAME == SECONDNAME)); // Output: false;
 